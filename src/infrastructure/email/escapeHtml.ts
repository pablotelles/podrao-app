/**
 * Escapes HTML special characters to prevent XSS in email templates.
 * Must be applied to ALL user-supplied strings interpolated in HTML.
 */
export function escapeHtml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}
